void solve(){
    int n; cin >> n;
    vi a(n), b(n);
    for(int i = 0; i < n; i++)
        cin >> a[i];
    for(int i = 0; i < n; i++)
        cin >> b[i];
    int p1 = 0, p2 = 0;
    sort(all(a), greater<int>());
    sort(all(b), greater<int>());
    ll ans = 0;
    for(int i = 0; i < n; i++){
        if(a[p1] > b[p2])
            ans += a[p1], p1++;
        else
            ans += b[p2], p2++;
    }
    cout << ans << '\n';
}

