void solve(){
    int x, y; cin >> x >> y;
    int ans = 0;
    for(int i = 1; i <= sqrt(min(x, y)); i++){
        if(x % i == 0 && y % i == 0){
            ans++;
            if(x % (min(x, y)/i) == 0 && y % (min(x, y)/i) == 0 && i != sqrt(min(x, y)))
                ans++;
        }
    }
    cout << ans;
}

