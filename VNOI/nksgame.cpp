void solve() {
    int n; cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    for (int i = 0; i < n; i++) cin >> b[i];
    int ans = INT_MAX;
    sort(all(b));
    for (int i = 0; i < n; i++) {
        auto it = lower_bound(all(b), -a[i]) - b.begin();
        if (it  < b.size()) {
            ans = min(ans, abs(b[it] + a[i]));
        }
        if (it - 1 >= 0) {
            ans = min(ans, abs(b[it - 1] + a[i]));
        }
    }
    cout << ans;
}

